/*
题目描述
给定一个double类型的浮点数base和int类型的整数exponent。求base的exponent次方。
 */
public class Solution {
    public static void main(String[] args){

    }
    public double Power(double base, int exponent) {
        return (double)Math.pow(base,exponent);
    }
}